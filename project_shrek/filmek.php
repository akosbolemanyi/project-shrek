            <p id="cim1">Shrek 1 (2001)</p>
            <p id="leiras1">Hol volt, hol nem volt, egy messzi mocsárban, meghitt magányban élt egyszer egy morcos ogre, akit Shreknek hívtak. Ám a zöld szörny nyugalmát és életét egy napon fenekestül felbolygatta egy különös esemény: mesebeli lények lepték el a mocsarat. A három vak egér futkározott a vacsoráján, a nagy, gonosz farkas feküdt az ágyában, a hét törpe Hófehérke koporsóját tette az asztalára, kunyhója előtt pedig ott nyüzsgött a három hajléktalan kismalac, akiket a gonosz Farquaad nagyúr űzött el otthonukból. Shrek tehát elment a nagyúrhoz, hogy visszakövetelje mocsarát, de ehhez előbb meg kell mentenie a szörnyű tűzokádó sárkány karmaiból Fiona királylányt, hogy Farquaad feleségül vehesse, és így király lehessen... A nagy kalandban társa is akad az ogrének: a szószátyár Szamár, aki a világ minden kincséért sem hajlandó befogni a száját. Hamarosan kiderül, hogy a sárkánynál sokkal nagyobb problémát jelent Fiona különös titka... A DreamWorks rajzfilmje fergeteges humorával, leleményes fordulataival, friss lendületességével egy időtálló moziszenzáció...</p>
    
            <p id="cim2">Shrek 2 (2004)</p>
            <p id="leiras2">A tűzokádó sárkány és a gonosz Farquaad nagyúr legyőzése után Shrek, a zöld öklű öldöklő, mindenki kedvenc ogréja ezúttal élete legnehezebb feladatával szembesül: a nászútról hazatérve el kell fogadtatnia magát apósával és anyósával. A nászutat követően ugyanis Shreket és Fionát meghívás várja a lány szüleitől Túl az Óperencián birodalmába. Felkerekednek hát a levakarhatatlan Szamárral, hogy kilépjenek az ünneplő tömeg elé, csakhogy Túl az Óperencián még senki nem tudja, hogy a királylány a varázslat megtörtével kissé máshogy néz ki. Harold, a király bizony másképpen képzelte el lánya jövőjét – és a sajátját is. És itt jön a képbe a varázslatos hatalmú Jótündér, az elbűvölő Szőke Herceg, no meg a legendás bérgyilkos, Csizmás Kandúr, akit a király bérel fel, hogy végezzen Shrekkel, de a macska kudarcot vall, sőt, összebarátkozik Shrekkel. Az ogre végül Fionáért hajlandó emberré változni a Jótündér egy varázsitala segítségével - ezt azonban kihasználja a Jótündér, Shreket tömlöcbe veti és azt hazudja, hogy a Szőke Herceg az emberré változott ogre...</p>
    
            <p id="cim3">Harmadik Shrek (2007)</p>
            <p id="leiras3">Nem mindenki akar király lenni, és ez különösen igaz a behemót, mocsárbűzös ogrékra. Amikor Shrek feleségül vette Fionát, eszébe sem jutott, hogy egyszer trónra akarják majd ültetni. Ám apósa, az öreg Harold király váratlanul megbetegszik, és Shreket tekinti a trón várományosának. A birodalom jövendő királya vagy talál maga helyett valaki mást a munkára, vagy élete hátralévő részét uralkodással kell töltenie. És ha ez még nem lenne elég, Fiona hercegnő is tartogat egy kis meglepetést Shrek számára. Shrek persze bepánikol a felelősségtől, és elhatározza, hogy felkutatja az egyetlen személyt, aki rajta kívül elfoglalhatja a trónt: nem mást, mint Fiona rég elveszett unokaöccsét, Artie-t, egy középkori iskolakerülőt. Amíg azonban az ogre távol van, esküdt ellensége, a Szőke Herceg visszatér, hogy elégtételt vegyen az őt ért sérelmekért...</p>
    
            <p id="cim4">Shrek a vége, fuss el véle (2010)</p>
            <p id="leiras4">Miután megvívott a gonosz sárkánnyal, megmentette a szép királylányt és a hozzá tartozó birodalmat, mitévő legyen egy ogre? Nos, ha Shrekről van szó, ő egy csapásra otthonülő családapa lett. Ahelyett, hogy a falusiakat rémisztgetné, mint régen, a kelletlen Shrek manapság még autogramot is ad a vasvilládra. Mi lett a bömbölő ogréval? Shrek a régi szép idők után sóvárog, amikor még "igazi ogre" lehetett, és ezért sikerül őt rászednie Rumpelstiltskinnek, a behízelgő modorú fondorkodónak, aki aláírat vele egy megállapodást. Így aztán Shrek egyik napról a másikra egy eltorzított Túl az Óperencián közepén találja magát, ahol az ogrékat üldözik, ahol Rumpelstiltskin a király, és Shrek és Fiona soha nem is találkoztak. Most Shreken a sor, hogy kiköszörülje a csorbát, megmentse a barátait, helyreállítsa a kizökkent világot és visszaszerezze egyetlen, igaz szerelmét.</p>
