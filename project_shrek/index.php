                    A Shrek 2001-ben bemutatott amerikai 3D-s számítógépes animációs film, amely a Shrek-sorozat első része. A franchise közönségnél, így ezáltal a kasszáknál elért sikereinek köszönhetően megélt még 3 újabb felvonást 9 éven belül. <br>
                    Miért ne élhetne boldogan a csúf, amíg meg nem hal? Erre a gondolatra épül a mára már mindenki által ismert Shrek univerzum. Ez minden addigi animációs filmtől eltért, ez lett a kulcs, amiért kiépült egy konkrét kultúra köré. Szórakoztató, korszakalkotó zenékkel teli, nem csak gyerekeknek, de legalább ugyanannyira felnőtteknek is szólnak. Mindenkinek látnia kell, és nem csak egyszer.
