                <p class="szoveg">
                    A melák ogre története nem 2001-ben, hanem 1990-ben kezdődött. Ekkor jelent meg ugyanis az a mindössze 30 oldalas mesekönyv, mely a film alapjául szolgált. Az óriási, kézzel rajzolt képekkel tarkított kiadványba laponként 6-7 sornál nem sokkal több történet fért, szóval dacára főhőse terebélyességének, maga a sztori meglehetősen soványka volt. Szerzője az osztrák felmenőkkel bíró William Steig, aki a mesekönyv megjelenésének idején már 82 éves is elmúlt. Steig receptje tulajdonképpen rendkívül egyszerű volt: nehezen kedvelhető, morcos főszereplőjéből csinált hőst, miközben sorra vette a klasszikus európai mesék kliséit csak azért, hogy jól kiforgassa őket. Shrek ebben a változatban még csak egy szárnyait próbálgató lusta felnőtt, akit szülei kipaterolnak a házukból, mondván, ideje megtanulnia megállni a saját lábán. Egy boszorkány jóslata nyomán találkozik egy beszélő szamárral, akivel közösen elmegy egy kastélyhoz kiszabadítani egy, még nálánál is rondább királylányt, akivel aztán "borzasztóan élnek, amíg meg nem halnak, miközben mindenkire a frászt hozzák"…  
